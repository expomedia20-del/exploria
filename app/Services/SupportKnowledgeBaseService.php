    public function forUser(User $user): array
    {
        $role = $user->role;
        $promptGroups = $this->promptGroups($role);

        if ($role === UserRole::Admin) {
            $promptGroups = array_merge($promptGroups, $this->centralAdminExtraPromptGroups());
        }

        return [
            'roleContext' => $this->roleContext($role),
            'promptGroups' => $promptGroups,
            'supportPriorities' => $this->supportPriorities($role),
            'quickActions' => $this->quickActions($role),
            'checklist' => $this->checklist($role),
            'handoffNotes' => $this->handoffNotes($role),
        ];
    }

